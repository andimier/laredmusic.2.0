				La Red Music, de la mano de la Cámara de Comercio de Bogotá, trae por primera vez al país 
				Bogotá Market Music, un evento especializado, que busca abrir canales para el contenido musical 
				colombiano por las vías alternativas de la industria musical, que nacieron debido a la 
				poca venta de los discos. <br /><br />
				
				Es así como más de 200 artistas, entre cantantes, músicos, compositores y demás tendrán la oportunidad de 
				conocer como rentabilizar la industria musical, y por ende cómo generar ganancias a través de lo que hacen.
				<br /><br />
				Según los productores ejecutivos del evento, ahora el negocio se encuentra en los conciertos, 
				un canal que es exclusivo a los artistas más reconocidos y que deja por fuera a quienes apenas comienzan.
				
				“La industria de la música en los últimos años se ha quedado sin producto. 
				Antes ésta estaba soportada en la venta del disco físico, pero con la aparición de las 
				plataformas digitales y con el tema de la piratería hace algunos años la industria ya no factura, 
				y por esto y de alguna manera se ha venido acabando el negocio”, afirmó María Isabel de los Ríos, 
				productora ejecutiva del evento y fundadora de Red Music.
				<br /><br />

				Por lo cual, las alianzas entre las marcas y el cantante, que permiten una relación de gana
				y gana al incrementar ventas y reconocimiento de parte y parte, además de generar un lazo 
				emotivo con la marca; la distribución de las composiciones en espacios mediáticos, 
				bien sea incluyendo una parte de una canción en películas, novelas, comerciales o 
				vender la composición para otro cantante; y la puesta en escena mediante circuitos, 
				que es una especie de giras por escenarios pequeños y en ciertas rutas establecidas en cada ciudad,
				son los nuevos negocios de la música que se presentarán en el Bogotá Music Market 2012.
				<br /><br /> 
				“Quisimos traer este ‘mercado’ porque las disqueras en el país, ya no firman tantos artistas como antes.
				Y porque estas nuevas estrategias aún no son conocidas en el sector", aseguró Alain Corona, co-fundador de La Red Music.
				<br /><br /> 

				Bogotá Music Market se llevará a cabo el 25 y 26 de septiembre, 
				en la Cámara de Comercio con sede en Chapinero. 
				En donde se otorgará charlas y agendamientos con ejecutivos de las marcas que el 
				invitado requiera, de modo que el artista además de conocer las bases de estos nuevos 
				caminos en la industria, podrá tener acceso a contactos importantes de la industria, 
				para concretar los negocios.
		
