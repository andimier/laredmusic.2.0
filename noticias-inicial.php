         		• Con el fin de crear una nueva forma de hacer negocios que permita impulsar las ventas 
				en la industria de la música en Colombia, la Cámara de Comercio de Bogotá realizará el 25 
				y 26 de septiembre de 2012, la primera versión del Bogotá Music Market  (BoMM) 2012.<br />
				• Desde el próximo 23 de julio y hasta el 31 de agosto estarán abiertas las inscripciones para realizar la preselección de los participantes. <br />
				•	Podrán participar artistas, compositores y productores independientes o a través de sus representantes, disqueras o editoras.<br /><br />
				
				Bogotá, julio 18 de 2012. Por primera vez en Colombia se llevará a cabo el Bogotá Music Market  (BoMM) 2012,  iniciativa de la Cámara 
				de Comercio de Bogotá (CCB) que ofrece un espacio de oportunidades para que los artistas colombianos 
				presenten sus producciones, composiciones o shows en vivo a cerca de diez marcas nacionales 
				e internacionales, cinco editoras internacionales y algunos circuitos locales.<br /><br />
				
				Entre el próximo 23 de julio y el 31 de agosto estarán abiertas las inscripciones, que serán totalmente gratuitas y 
				deberán realizarse de manera virtual en www.bogotamusicmarket.com.  Aquellos artistas que aplican entrarán en 
				un proceso de selección para determinar los cerca de 200 artistas que estarán presentes 
				el 25 y el 26 de septiembre en el BOMM.<br /><br />
				
				Esta iniciativa está dirigida a:<br />
				•	Artistas cuyos sencillos y/o álbumes hayan sido lanzados durante los dos últimos años.<br />
				•	Compositores y Productores musicales, dueños de sus obras.<br />
				•	Artistas que quieran inscribir sus shows en vivo. <br />
				•	Podrán participar personas naturales y jurídicas en diversas categorías: <br />
					-	Artista Independiente: Cantautor o Intérprete <br />
					-	Compositores y Productores Musicales Independientes o a través de su editora o representante legal<br />
					-	Artistas a través de sus Sellos o Disqueras<br />
					-	Artistas a través de sus Managers o Representantes Legales<br /><br />
					
				La presidenta de la CCB, Consuelo Caldas, asegura que el BoMM es el primer espacio de networking 
				creado en el país con el fin de estimular la creación de nuevos negocios alrededor de la industria musical.<br /><br />
				
				“El BoMM se convertirá en una plataforma de promoción y actualización profesional, así como un espacio de encuentro para realizar acuerdos, 
				alianzas y negocios de la industria de la música”, aseguró Caldas.<br /><br />
				
				Agregó que así como el Bogotá Audiovisual Market (BAM 2012) obtuvo buenos resultados (ventas de películas Colombianas 
				a distribuidores internacionales e inversión extranjera para proyectos Colombianos en etapa de desarrollo), 
				el BoMM promete ser la plataforma por excelencia para impulsar la industria de la música en Colombia.<br /><br /><br />

				
				
				Espacios del BoMM<br />
				
				El BoMM desarrollará diversos espacios de participación para el intercambio entre los participantes nacionales e internacionales:<br /><br />

				• One - On - One Meetings: un espacio privilegiado para exponer de primera mano proyectos musicales en búsqueda de 
				negocios de patrocinios, promociones cruzadas, “product placement” y demás formas de alianzas con marcas o agencias nacionales 
				e internacionales. Así mismo, el formato para la  búsqueda de negocios de firma de artistas y obras 
				musicales con editoras nacionales e internacionales, así como para  la promoción de sus shows, a través 
				de empresarios de circuitos, en búsqueda de oportunidades comerciales para el artista y su música. <br />
				
				• BoMM Talks: relevantes profesionales internacionales, de la industria de la música,  realizarán paneles y 
				seminarios en torno a los temas de actualidad de  la industria de la música y los nuevos negocios alrededor de ésta.  
				A través de este espacio, los asistentes podrán complementar su participación con encuentros académicos especializados 
				según las diferentes áreas del sector, cuyo propósito es guiarlos y prepararlos para abordar los nuevos negocios 
				de la Industria, que se manejan en otras partes del mundo. <br />
				• Espacio - Protege tus Obras: un espacio privilegiado para los participantes en los One - On - 
				One Meetings, en el cual contarán con asesoría legal acerca de derechos de autor, distribución de regalías 
				y en general lineamientos acerca de como funcionan a nivel contractual los posibles negocios que se 
				realizarán en el mercado en todos sus frentes. 

